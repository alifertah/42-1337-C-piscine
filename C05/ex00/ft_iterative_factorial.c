#include <unistd.h>

int	ft_iterative_factorial(int nb)
{
	int	i;

	i = nb;
	if (nb == 1 || nb == 0)
	{
		return (1);
	}
	while (i > 1)
	{
		nb = nb * (i - 1);
		i--;
	}
	return (nb);
}
